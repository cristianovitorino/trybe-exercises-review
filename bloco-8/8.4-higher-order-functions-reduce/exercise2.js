// Crie uma string com os nomes de todas as pessoas autoras.
const expectedResult = "George R. R. Martin, J. R. R. Tolkien, Isaac Asimov, Frank Herbert, Stephen King, H. P. Lovecraft.";

function reduceNames() {
  const names = books.reduce((acc, book, index, array) => // reduce recebe um acumulador, o valor atual e o índice do array
    index === array.length - 1 ? // Se for o último elemento do array
      `${acc} ${book.author.name}.` : // Retorna o nome do autor
      `${acc} ${book.author.name},`, // Se não for (ou enquanto não for), concatena o nome do autor com vírgula
    ''); // Inicia a string com vazio
  return names.trim(); // Retorna a string sem espaços no início e no fim
}

assert.strictEqual(reduceNames(), expectedResult);
